#include "libft.h"
#include <stdlib.h>

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*new;
	int		i;
	int		size;

	i = 0;
	if (s == NULL)
		return (NULL);
	size = ft_strlen(s);
	if (!(new = (char *)malloc(size + 1)))
		return (NULL);
	while (s[i] != '\0')
	{
		new[i] = f(s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
